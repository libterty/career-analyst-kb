        ("第四願", "不貪財", "修道之人應淡泊名利，不貪不義之財。財乃身外之物，貪財易招業障，阻礙修行進程。"),
        ("第五願", "不好色", "道親應守身如玉，遵守男女之防，不沉溺於色欲之中。清淨身心乃修行之基礎。"),
        ("第六願", "不戀酒", "酒能亂性，飲酒過量易失去理智，造下惡業。修道之人應戒酒或節制，保持清醒之心。"),
        ("第七願", "不吸食鴉片及一切迷魂藥物", "毒品戕害身心，使人沉淪，嚴重障礙修行。道親應嚴格戒除一切麻醉性藥物。"),
