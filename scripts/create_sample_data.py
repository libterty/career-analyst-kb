#!/usr/bin/env python3
"""生成範例一貫道典籍 DOCX 文件，放入 data/raw/ 供測試匯入使用。

此腳本會產生四份仿真的一貫道典籍文件，
讓開發者無需真實典籍也能測試完整的 RAG 匯入與問答流程。

產生四份文件：
  1. 基礎教義總覽.docx   — 天道概說、三寶要義、求道儀式、五教同源
  2. 十條大願詳解.docx   — 十條大願逐條說明與發願意義
  3. 修行方法指引.docx   — 日課功課、清口素食、五常實踐、三省吾身
  4. 道場規範手冊.docx   — 佛堂介紹、進入禮儀、活動規範、壇主職責

Usage:
    python3.11 scripts/create_sample_data.py
    python3.11 scripts/create_sample_data.py --output-dir data/raw/
"""
from __future__ import annotations

import argparse
import os
import sys
from pathlib import Path

# 將專案根目錄加入 Python 路徑
sys.path.insert(0, os.path.dirname(os.path.dirname(__file__)))

from docx import Document
from docx.shared import Pt, RGBColor
from docx.enum.text import WD_ALIGN_PARAGRAPH


# ── 輔助函式 ──────────────────────────────────────────────────────────

def _heading(doc: Document, text: str, level: int = 1) -> None:
    """新增章節標題段落（靠左對齊）。"""
    p = doc.add_heading(text, level=level)
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT


def _body(doc: Document, text: str) -> None:
    """新增正文段落（段後留 6pt 間距）。"""
    p = doc.add_paragraph(text)
    p.paragraph_format.space_after = Pt(6)


def _divider(doc: Document) -> None:
    """新增分隔線（用於視覺區隔）。"""
    doc.add_paragraph("─" * 40)


